    function myFunction() {
    var element = document.getElementById("myDIV");
    element.classList.add("mystyle");
    element.classList.remove("mystyle2");
    element.classList.remove("mystyle3");
    element.classList.remove("mystyle4");
    element.classList.remove("mystyle5");
    document.getElementById("myDIV2").style.display = "none";  //hide
    document.getElementById("myDIV3").style.display = "none";  //hide
    document.getElementById("myDIV4").style.display = "none";  //hide
    document.getElementById("myDIV5").style.display = "none";  //hide
    document.getElementById("myDIV").style.display = "block"; //show
    document.getElementById("myDIV").style.display = ""; 	   //show
    }
